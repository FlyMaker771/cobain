    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID User");
        model.addColumn("Nama");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("ID Outlet");
        model.addColumn("Role");
        table_user.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from user");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                  rs.getInt("id_user"),
                  rs.getString("nama"),
                  rs.getString("username"),
                  rs.getString("password"),rs.getInt("id_outlet"),rs.getString("role"),
                  
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_nama.setText("");
        text_id.setText("");
        text_password.setText("");
        text_username.setText("");
        text_search.setText("");
        
    }
    public void Search(String cari){
        model = new DefaultTableModel();
        model.addColumn("ID User");
        model.addColumn("Nama");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("ID Outlet");
        model.addColumn("Role");
        table_user.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from user where nama like '"+ cari +"%' or username like '"+cari+"%'");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                  rs.getInt("id_user"),
                  rs.getString("nama"),
                  rs.getString("username"),
                  rs.getString("password"),rs.getInt("id_outlet"),rs.getString("role"),
                  
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_nama.setText("");
        text_id.setText("");
        text_password.setText("");
        text_username.setText("");
        
    }

