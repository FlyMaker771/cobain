    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            int opsi = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Mengubah Data?");
            switch(opsi){
                case JOptionPane.YES_OPTION:
                    this.stat = k.getCon().prepareStatement("update user set nama=?, username=?, password=?, id_outlet=?, role=? where id_user=?");
                    this.stat.setString(1, text_nama.getText());
                    this.stat.setString(2, text_username.getText());
                    this.stat.setString(3, text_password.getText());
                    this.stat.setString(4, text_idOutlet.getText());
                    this.stat.setString(5, combo_role.getSelectedItem().toString());
                    this.stat.setInt(6, Integer.parseInt(text_id.getText()));
                    this.stat.executeUpdate();
                    refreshTable();
                    JOptionPane.showMessageDialog(null, "<html><head><style>h2{text-align: center;}</style></head><body><h2>DATA BERHASIL DI UPDATE!</h2></body></html>");
                    btn_delete.setEnabled(false);
                    btn_update.setEnabled(false);
                    btn_input.setEnabled(true);
                    break;
                 case JOptionPane.NO_OPTION:
                     break;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

