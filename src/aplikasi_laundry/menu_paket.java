    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputActionPerformed
        // TODO add your handling code here:
        try {
            paket p = new paket();
            this.stat = k.getCon().prepareStatement("insert into paket values(?,?,?,?,?)");
            this.stat.setInt(1, p.id_paket);
            this.stat.setInt(2, id_outlet);
            this.stat.setString(3, p.jenis);
            this.stat.setString(4, p.namaPaket);
            this.stat.setString(5, p.harga);
            this.stat.executeUpdate();
            refreshTable();
             JOptionPane.showMessageDialog(null, "<html><head><style>h2{text-align: center;}</style></head><body><h2>DATA BERHASIL DI SIMPAN!</h2></body></html>");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_inputActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            paket p = new paket();
            int opsi = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Mengubah Data?");
            switch(opsi){
                case JOptionPane.YES_OPTION:
                    this.stat = k.getCon().prepareStatement("update paket set jenis=?, nama_paket=?, harga=? where id_paket=?");
                    this.stat.setString(1, p.jenis);
                    this.stat.setString(2, p.namaPaket);
                    this.stat.setString(3, p.harga);
                    this.stat.setInt(4, Integer.parseInt(text_id.getText()));
                    
                    this.stat.executeUpdate();
                    refreshTable();
                    JOptionPane.showMessageDialog(null, "<html><head><style>h2{text-align: center;}</style></head><body><h2>DATA BERHASIL DI UPDATE!</h2></body></html>");
                    btn_delete.setEnabled(false);
                    btn_update.setEnabled(false);
                    btn_input.setEnabled(true);
                    break;
                case JOptionPane.NO_OPTION:
                    break;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        try {
            int opsi = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus Data?");
            switch(opsi){
                case JOptionPane.YES_OPTION:
                    this.stat = k.getCon().prepareStatement("delete from paket where id_paket=?");
                    this.stat.setInt(1, Integer.parseInt(text_id.getText()));
                    this.stat.executeUpdate();
                    refreshTable();
                    JOptionPane.showMessageDialog(null, "<html><head><style>h2{text-align: center;}</style></head><body><h2>DATA BERHASIL DI DELETE!</h2></body></html>");
                    btn_delete.setEnabled(false);
                    btn_update.setEnabled(false);
                    btn_input.setEnabled(true);
                    break;
                case JOptionPane.NO_OPTION:
                    break;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

