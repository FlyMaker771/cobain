    class paket{
        int id_paket;
        String jenis, namaPaket, harga;
        
        public paket(){
            this.id_paket = 0;
            this.jenis = combo_paket.getSelectedItem().toString();
            this.namaPaket = text_namaPaket.getText();
            this.harga = text_harga.getText();
            
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Paket");
        model.addColumn("ID Outlet");
        model.addColumn("Jenis");
        model.addColumn("Nama Paket");
        model.addColumn("Harga");
        table_paket.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from paket");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                  rs.getInt("id_paket"),
                  rs.getInt("id_outlet"),
                  rs.getString("jenis"),
                  rs.getString("nama_paket"),rs.getInt("harga"),
                  
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_namaPaket.setText("");
        text_id.setText("");
        text_harga.setText("");
        text_idOutlet.setText("");
        
        
    }
    
    

