    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        try {
            int opsi = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus Data?");
            switch(opsi){
                case JOptionPane.YES_OPTION:
                    this.stat = k.getCon().prepareStatement("delete from member where id_member=?");
                    this.stat.setInt(1, Integer.parseInt(text_id.getText()));
                    this.stat.executeUpdate();
                    refreshTable();
                    JOptionPane.showMessageDialog(null, "<html><head><style>h2{text-align: center;}</style></head><body><h2>DATA BERHASIL DI DELETE!</h2></body></html>");
                    btn_delete.setEnabled(false);
                    btn_update.setEnabled(false);
                    btn_input.setEnabled(true);
                    break;
                case JOptionPane.NO_OPTION:
                    break;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

