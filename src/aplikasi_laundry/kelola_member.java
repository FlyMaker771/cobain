    class member{
        int id_member;
        String nama, alamat, jenis_kelamin, tlp;
        
        public member(){
           this.id_member = 0;
           this.nama = text_nama.getText();
           this.alamat = text_alamat.getText();
           this.jenis_kelamin = combo_role.getSelectedItem().toString();
           this.tlp = text_telpon.getText();
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Member");
        model.addColumn("Nama Lengkap");
        model.addColumn("Alamat");
        model.addColumn("Jenis Kelamin");
        model.addColumn("No Handphone");
        tbl_outlet.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from member");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                  rs.getInt("id_member"),
                  rs.getString("nama"),
                  rs.getString("alamat"),
                  rs.getString("jenis_kelamin"),
                  rs.getString("tlp"),
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_id.setText("");
        text_telpon.setText("");
        text_nama.setText("");
        text_alamat.setText("");
        
    }
    
    
    public void search(String cari){
        model = new DefaultTableModel();
        model.addColumn("ID Member");
        model.addColumn("Nama Lengkap");
        model.addColumn("Alamat");
        model.addColumn("Jenis Kelamin");
        model.addColumn("No Handphone");
        tbl_outlet.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from member where nama like '"+cari+"%' or tlp like '"+cari+"%'");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
                Object[] data = {
                  rs.getInt("id_member"),
                  rs.getString("nama"),
                  rs.getString("alamat"),
                  rs.getString("jenis_kelamin"),
                  rs.getString("tlp"),
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_id.setText("");
        text_telpon.setText("");
        text_nama.setText("");
        text_alamat.setText("");
        
    }

